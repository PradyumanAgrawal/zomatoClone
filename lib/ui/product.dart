class Product
{
  String name;
  String description;
  String imgPath;
  bool isFav;
  String price;

  Product({String name, String description, String imgPath, bool isFav, String price})
  {
    this.name = name;
    this.description = description;
    this.imgPath = imgPath;
    this.isFav = isFav;
    this.price = price;
  }

}